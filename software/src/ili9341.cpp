bool Driver::Init(DaisySeed& seed)
{
    rotation_ = 0;

    dc_pin_.Init(seed.GetPin(Pins::DISP_DC), GPIO::Mode::OUTPUT);
    rst_pin_.Init(seed.GetPin(Pins::DISP_RST), GPIO::Mode::OUTPUT);
    cs_pin_.Init(seed.GetPin(Pins::DISP_CS), GPIO::Mode::OUTPUT);
    bl_pin_.Init(seed.GetPin(Pins::DISP_PWM), GPIO::Mode::OUTPUT);

    cs_pin_.Write(true);
    dc_pin_.Write(true);

    SpiHandle::Config spi_cfg;
    spi_cfg.periph         = SpiHandle::Config::Peripheral::SPI_1;
    spi_cfg.mode           = SpiHandle::Config::Mode::MASTER;
    spi_cfg.direction      = SpiHandle::Config::Direction::TWO_LINES_TX_ONLY;
    spi_cfg.clock_polarity = SpiHandle::Config::ClockPolarity::LOW;
    spi_cfg.clock_phase    = SpiHandle::Config::ClockPhase::ONE_EDGE;
    spi_cfg.baud_prescaler = SpiHandle::Config::BaudPrescaler::PS_4;
    spi_cfg.nss            = SpiHandle::Config::NSS::SOFT;
    spi_cfg.datasize       = 8;
    spi_cfg.pin_config.sclk = seed.GetPin(Pins::DISP_SCK);
    spi_cfg.pin_config.mosi = seed.GetPin(Pins::DISP_MOSI);
    spi_cfg.pin_config.miso = Pin();
    spi_cfg.pin_config.nss  = Pin();

    spi_.Init(spi_cfg);

    rst_pin_.Write(true);
    System::Delay(5);
    rst_pin_.Write(false);
    System::Delay(20);
    rst_pin_.Write(true);
    System::Delay(150);

    WriteCommand(Cmd::SWRESET);
    System::Delay(150);

    WriteCommand(Cmd::SLPOUT);
    System::Delay(150);

    WriteCommand(Cmd::PWCTR1);
    WriteData(0x23);

    WriteCommand(Cmd::PWCTR2);
    WriteData(0x10);

    WriteCommand(Cmd::VMCTR1);
    WriteData(0x3E);
    WriteData(0x28);

    WriteCommand(Cmd::VMCTR2);
    WriteData(0x86);

    WriteCommand(Cmd::MADCTL);
    WriteData(MX | BGR);

    WriteCommand(Cmd::PIXFMT);
    WriteData(0x55);

    WriteCommand(Cmd::FRMCTR1);
    WriteData(0x00);
    WriteData(0x18);

    WriteCommand(Cmd::DFUNCTR);
    WriteData(0x08);
    WriteData(0x82);
    WriteData(0x27);

    WriteCommand(Cmd::GMCTRP1);
    static const uint8_t pgamma[] = {
        0x0F,0x31,0x2B,0x0C,0x0E,0x08,0x4E,0xF1,
        0x37,0x07,0x10,0x03,0x0E,0x09,0x00
    };
    for(uint8_t i = 0; i < sizeof(pgamma); i++)
        WriteData(pgamma[i]);

    WriteCommand(Cmd::GMCTRN1);
    static const uint8_t ngamma[] = {
        0x00,0x0E,0x14,0x03,0x11,0x07,0x31,0xC1,
        0x48,0x08,0x0F,0x0C,0x31,0x36,0x0F
    };
    for(uint8_t i = 0; i < sizeof(ngamma); i++)
        WriteData(ngamma[i]);

    WriteCommand(Cmd::DISPON);
    System::Delay(100);

    SetBacklight(200);

    return true;
}

void Driver::FillScreen(uint16_t color)
{
    FillRect(0, 0, WIDTH, HEIGHT, color);
}

void Driver::FillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                      uint16_t color)
{
    if(x >= WIDTH || y >= HEIGHT)
        return;
    if(x + w > WIDTH)  w = WIDTH - x;
    if(y + h > HEIGHT) h = HEIGHT - y;

    SetAddressWindow(x, y, x + w - 1, y + h - 1);

    uint8_t hi = color >> 8;
    uint8_t lo = color & 0xFF;

    cs_pin_.Write(false);
    dc_pin_.Write(true);

    uint32_t total = (uint32_t)w * h;
    uint8_t buf[512];
    for(uint16_t i = 0; i < 256; i++)
    {
        buf[i * 2]     = hi;
        buf[i * 2 + 1] = lo;
    }
    while(total > 0)
    {
        uint32_t chunk = (total > 256) ? 256 : total;
        spi_.BlockingTransmit(buf, chunk * 2);
        total -= chunk;
    }

    cs_pin_.Write(true);
}

void Driver::DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
    if(x >= WIDTH || y >= HEIGHT)
        return;
    SetAddressWindow(x, y, x, y);
    WriteData16(color);
}

void Driver::DrawHLine(uint16_t x, uint16_t y, uint16_t w, uint16_t color)
{
    FillRect(x, y, w, 1, color);
}

void Driver::DrawVLine(uint16_t x, uint16_t y, uint16_t h, uint16_t color)
{
    FillRect(x, y, 1, h, color);
}

void Driver::DrawRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                      uint16_t color)
{
    DrawHLine(x, y, w, color);
    DrawHLine(x, y + h - 1, w, color);
    DrawVLine(x, y, h, color);
    DrawVLine(x + w - 1, y, h, color);
}

void Driver::DrawChar(uint16_t x, uint16_t y, char c,
                      uint16_t fg, uint16_t bg)
{
    if(c < 32 || c > 126)
        c = '?';

    const uint8_t* glyph = &font5x7[(c - 32) * 5];

    for(uint8_t col = 0; col < 5; col++)
    {
        uint8_t line = glyph[col];
        for(uint8_t row = 0; row < 7; row++)
        {
            uint16_t color = (line & (1 << row)) ? fg : bg;
            FillRect(x + col * 2, y + row * 2, 2, 2, color);
        }
    }
    FillRect(x + 10, y, 2, 14, bg);
}

void Driver::DrawString(uint16_t x, uint16_t y, const char* str,
                        uint16_t fg, uint16_t bg)
{
    uint16_t cx = x;
    while(*str)
    {
        if(*str == '\n')
        {
            cx = x;
            y += 16;
        }
        else
        {
            DrawChar(cx, y, *str, fg, bg);
            cx += 12;
        }
        str++;
        if(cx + 12 > WIDTH)
        {
            cx = x;
            y += 16;
        }
    }
}

void Driver::SetBacklight(uint8_t brightness)
{
    // pwm later maybe
    bl_pin_.Write(brightness > 0);
}

void Driver::SetRotation(uint8_t rotation)
{
    rotation_ = rotation % 4;
    WriteCommand(Cmd::MADCTL);
    switch(rotation_)
    {
    case 0:
        WriteData(MX | BGR);
        break;
    case 1:
        WriteData(MV | BGR);
        break;
    case 2:
        WriteData(MY | BGR);
        break;
    case 3:
        WriteData(MX | MY | MV | BGR);
        break;
    }
}

void Driver::WriteCommand(uint8_t cmd)
{
    cs_pin_.Write(false);
    dc_pin_.Write(false);
    spi_.BlockingTransmit(&cmd, 1);
    cs_pin_.Write(true);
}

void Driver::WriteData(uint8_t data)
{
    cs_pin_.Write(false);
    dc_pin_.Write(true);
    spi_.BlockingTransmit(&data, 1);
    cs_pin_.Write(true);
}

void Driver::WriteData16(uint16_t data)
{
    uint8_t buf[2] = {(uint8_t)(data >> 8), (uint8_t)(data & 0xFF)};
    cs_pin_.Write(false);
    dc_pin_.Write(true);
    spi_.BlockingTransmit(buf, 2);
    cs_pin_.Write(true);
}

void Driver::SetAddressWindow(uint16_t x0, uint16_t y0,
                              uint16_t x1, uint16_t y1)
{
    WriteCommand(Cmd::CASET);
    WriteData16(x0);
    WriteData16(x1);

    WriteCommand(Cmd::PASET);
    WriteData16(y0);
    WriteData16(y1);

    WriteCommand(Cmd::RAMWR);
}

